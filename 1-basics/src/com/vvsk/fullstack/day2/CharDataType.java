package com.vvsk.fullstack.day2;

/** Example Program to create Char Datatype variable */
public class CharDataType {

    public static void main(String args[]) {
		
		// Number 65 is ASCII code of A
		char a = 'A'; 
		// Number 66 is ASCII code of B
		char b = 66; 
		// Number 67 is ASCII code of C
		char c = 67; 
				
		System.out.println("Storing character A,B,C : "+a+" "+b+" "+c);
		
	}

}
