package com.vvsk.fullstack.day2;

/** Example Program to create Boolean Datatype variable */
public class BooleanDataType {

    public static void main(String[] args) {
		
		// Declare a boolean variable as false
		boolean answer = false;
		
		System.out.println("Initial Value "+answer);
		
		
		if(answer) {
			System.out.println("Answer is correct ");
		}
		else {
			System.out.println("Answer is wrong ");
		}

	}

}
