package com.vvsk.fullstack.day3;

public class AssignmentOperations {

    public static void main(String args[]) {
		
		// Variable Declaration Statements
	    int num1 = 10, num2=25;
	    
	    
	    System.out.println("= Output: "+num2);
	    // num2 = num2 + num1
	    num2 += num1;
	    System.out.println("+= Output: "+num2);
	    
	    //num2 = num2 - num1
	    num2 -= num1;
	    System.out.println("-= Output: "+num2);
	    
	    //num2 = num2 * num1
	    num2 *= num1;
	    System.out.println("*= Output: "+num2);
	    
	    
	    // num2 = num2 / num1
	    num2 /= num1;
	    System.out.println("/= Output: "+num2);
	    
	    
	    // num2 = num2 % num1
	    num2 %= num1;
	    System.out.println("%= Output: "+num2);
	  }

}
