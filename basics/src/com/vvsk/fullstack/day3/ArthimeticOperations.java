package com.vvsk.fullstack.day3;

public class ArthimeticOperations {

    /* Write Java Program which prints sum of given two numbers */
	/* Write Java Program to find the difference of given two numbers */
	/* Write Java Program which prints product of given two numbers */
	/* Write Java Program which divides the first number by second number */
	/* Write Java Program which prints remainder using %(Modulus operator */
	
	public static void main(String args[]) {
		
		// Integer Datatype 
	    int num1 = 25;
	    int num2 = 10;
	    
	    // Float Datatype
	    float a = 25.25f;
	    float b = 10.10f;
	    
	    
	    
	    System.out.println("num1+num2: " + (num1 + num2) );
	    System.out.println("num1-num2: " + (num1 - num2) );
	    System.out.println("num1*num2: " + (num1 * num2) );
	    System.out.println("num1/num2: " + (num1 / num2) );
	    System.out.println("num1%num2: " + (num1 % num2) );
	    
	    
	    System.out.println("a+b: " + (a + b) );
	    System.out.println("a-b: " + (a - b) );
	    System.out.println("a*b: " + (a * b) );
	    System.out.println("a/b: " + (a / b) );
	    System.out.println("a%b: " + (a % b) );
	  }

}
